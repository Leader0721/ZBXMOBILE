package com.zbxn.pub.frame.common;

public class ToolbarParams {
    /**
     * 获取主题中定义的toolbar的高度
     */
    public int toolBarSize;
    /**
     * 主题中定义的悬浮标志
     */
    public boolean overlay = false;
    /**
     * toolbar的背景色
     */
    public int colorResId;

    /**
     * 是否显示Toolbar底部的阴影
     */
    public boolean shadowEnable = true;
}